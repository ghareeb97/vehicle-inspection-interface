// Function to get language from URL parameter
function getUrlParameter(name) {
    name = name.replace(/[\[]/, "\\[").replace(/[\]]/, "\\]");
    const regex = new RegExp("[\\?&]" + name + "=([^&#]*)");
    const results = regex.exec(location.search);
    return results === null ? "" : decodeURIComponent(results[1].replace(/\+/g, " "));
}

// Function to set document language and direction
function setDocumentLangDir(lang) {
    document.documentElement.lang = lang.split("_")[0];
    document.documentElement.dir = lang === "ar_ar" ? "rtl" : "ltr";
}

// Initialize translations
async function initializeTranslations() {
    if (translationsInitialized) {
        return Promise.resolve();
    }

    const lang = getUrlParameter("lang") || "en_us";
    setDocumentLangDir(lang);

    try {
        console.log('Initializing translations with language:', lang);
        console.log('Available translations:', {
            en_us: combined_translations.en_us ? 'Available' : 'Missing',
            ar_ar: combined_translations.ar_ar ? 'Available' : 'Missing'
        });

        await i18nextInstance.init({
            lng: lang,
            debug: true,
            resources: {
                en_us: { translation: combined_translations.en_us.translation },
                ar_ar: { translation: combined_translations.ar_ar.translation }
            }
        });

        console.log('Translations loaded successfully');
        translationsInitialized = true;
        applyTranslations(); // Apply initial translations
    } catch (error) {
        console.error('Translation initialization error:', error);
    }
}


// Apply translations to elements with data-i18n attribute
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (key) { // Add this check to ensure key is not null or undefined
            const translatedText = i18nextInstance.t(key);
            if (translatedText === key) {
                console.warn(`Missing translation for key: ${key}`);
                // Keep the original text content instead of showing the key
                return; // Don't update the element
            }
            element.textContent = translatedText;
        } else {
            console.warn("  Element", element, "has data-i18n but no key value."); // Optional: Warn if data-i18n is present but empty
        }
    });
    console.log("applyTranslations() function finished."); // Add this line

    // Refresh issues dashboard if it exists and translations are applied
    if (typeof window.refreshDashboardLanguage === 'function') {
        window.refreshDashboardLanguage();
    }
}

// Translation function to be used in code
function translate(key) {
    if (!i18nextInstance || !key) return '';

    try {
        const translation = i18nextInstance.t(key);
        // Return empty string if translation is the same as the key (missing translation)
        return translation === key ? '' : translation;
    } catch (error) {
        console.error('Translation error for key:', key, error);
        return '';
    }
}

// Helper function for dynamic chart titles
function translateChartTitle(timePeriod) {
    const key = `Cost Per Kilometer Trend - ${timePeriod.charAt(0).toUpperCase() + timePeriod.slice(1)}ly View`;
    return translate(key) || key;
}

// Update language dynamically
function updateLanguage(newLang) {
    setDocumentLangDir(newLang);
    return i18nextInstance.changeLanguage(newLang).then(() => {
        applyTranslations();

        // Update active class on language buttons
        document.querySelectorAll('.language-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('onclick').includes(newLang)) {
                btn.classList.add('active');
            }
        });

        // Update summary text with new language templates
        updateSummaryText();

        // Update pagination display if it exists
        if (window.updateExpenseTable && typeof window.updateExpenseTable === 'function') {
            window.updateExpenseTable();
        }

        // Update chart labels with new language
        if (typeof window.updateChartLabels === 'function') {
            window.updateChartLabels();
        }

        // Refresh issues dashboard if it exists
        if (typeof window.refreshDashboardLanguage === 'function') {
            window.refreshDashboardLanguage();
        }
    });
}

// Function to update summary text based on current language
function updateSummaryText() {
    // Get the current values from the summary
    const summaryTextElement = document.getElementById('summaryText');
    const summaryVendorElement = document.getElementById('summaryVendorText');

    if (!summaryTextElement || !summaryVendorElement) return;

    // Check if we have window.summaryData (set by the dashboard initialization)
    if (window.summaryData) {
        // Use the data from window.summaryData
        const { vehicleCount, totalExpense, paidPercent, vendor } = window.summaryData;

        // Get the templates for the current language
        const summaryTemplate = translate('summaryTemplate') || "This report provides a comprehensive overview of our fleet expenses across {{vehicleCount}} vehicles. Total expenditure amounts to {{totalExpense}} with {{paidPercent}} of all invoices paid to date.";
        const vendorTemplate = translate('vendorTemplate') || "Primary service provider: {{vendor}}";

        // Replace placeholders with actual values
        const summaryText = summaryTemplate
            .replace('{{vehicleCount}}', vehicleCount)
            .replace('{{totalExpense}}', totalExpense)
            .replace('{{paidPercent}}', paidPercent);

        const vendorText = vendorTemplate
            .replace('{{vendor}}', vendor);

        // Update the summary text elements
        summaryTextElement.innerHTML = summaryText;
        summaryVendorElement.innerHTML = vendorText;
        return;
    }

    // If window.summaryData is not available, try to extract from current text
    // Extract values from the current text (this is a simplified approach)
    const vehicleCountMatch = summaryTextElement.textContent.match(/(\d+)\s+(?:vehicles|مركبة)/i);
    const totalExpenseMatch = summaryTextElement.textContent.match(/\$([0-9,.]+)/);
    const paidPercentMatch = summaryTextElement.textContent.match(/([0-9.]+)%/);
    const vendorMatch = summaryVendorElement.textContent.match(/(?:Primary service provider|مزود الخدمة الرئيسي):\s+(.+)$/);

    if (vehicleCountMatch && totalExpenseMatch && paidPercentMatch && vendorMatch) {
        const vehicleCount = vehicleCountMatch[1];
        const totalExpense = '$' + totalExpenseMatch[1];
        const paidPercent = paidPercentMatch[1] + '%';
        const vendor = vendorMatch[1];

        // Save the data for future use
        window.summaryData = { vehicleCount, totalExpense, paidPercent, vendor };

        // Get the templates for the current language
        const summaryTemplate = translate('summaryTemplate') || "This report provides a comprehensive overview of our fleet expenses across {{vehicleCount}} vehicles. Total expenditure amounts to {{totalExpense}} with {{paidPercent}} of all invoices paid to date.";
        const vendorTemplate = translate('vendorTemplate') || "Primary service provider: {{vendor}}";

        // Replace placeholders with actual values
        const summaryText = summaryTemplate
            .replace('{{vehicleCount}}', vehicleCount)
            .replace('{{totalExpense}}', totalExpense)
            .replace('{{paidPercent}}', paidPercent);

        const vendorText = vendorTemplate
            .replace('{{vendor}}', vendor);

        // Update the summary text elements
        summaryTextElement.innerHTML = summaryText;
        summaryVendorElement.innerHTML = vendorText;
    }
}

