// Function to get language from URL parameter
function getUrlParameter(name) {
    name = name.replace(/[\[]/, "\\[").replace(/[\]]/, "\\]");
    const regex = new RegExp("[\\?&]" + name + "=([^&#]*)");
    const results = regex.exec(location.search);
    return results === null ? "" : decodeURIComponent(results[1].replace(/\+/g, " "));
}

// Function to set document language and direction
function setDocumentLangDir(lang) {
    document.documentElement.lang = lang.split("_")[0];
    document.documentElement.dir = lang === "ar_ar" ? "rtl" : "ltr";
}

// Initialize translations
async function initializeTranslations() {
    if (translationsInitialized) {
        return Promise.resolve();
    }

    const lang = getUrlParameter("lang") || "en_us";
    setDocumentLangDir(lang);

    try {
        console.log('Initializing translations with language:', lang);
        console.log('Available translations:', {
            en_us: combined_translations.en_us ? 'Available' : 'Missing',
            ar_ar: combined_translations.ar_ar ? 'Available' : 'Missing'
        });

        await i18nextInstance.init({
            lng: lang,
            debug: true,
            resources: {
                en_us: { translation: combined_translations.en_us.translation },
                ar_ar: { translation: combined_translations.ar_ar.translation }
            }
        });

        console.log('Translations loaded successfully');
        translationsInitialized = true;
        applyTranslations(); // Apply initial translations
    } catch (error) {
        console.error('Translation initialization error:', error);
    }
}


// Apply translations to elements with data-i18n attribute
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (key) { // Add this check to ensure key is not null or undefined
            const translatedText = i18nextInstance.t(key);
            if (translatedText === key) {
                console.warn(`Missing translation for key: ${key}`);
                // Keep the original text content instead of showing the key
                return; // Don't update the element
            }
            element.textContent = translatedText;
        } else {
            console.warn("  Element", element, "has data-i18n but no key value."); // Optional: Warn if data-i18n is present but empty
        }
    });
    console.log("applyTranslations() function finished."); // Add this line

    // Refresh issues dashboard if it exists and translations are applied
    if (typeof window.refreshDashboardLanguage === 'function') {
        window.refreshDashboardLanguage();
    }
}

// Translation function to be used in code
function translate(key) {
    if (!i18nextInstance || !key) return '';

    try {
        const translation = i18nextInstance.t(key);
        // Return empty string if translation is the same as the key (missing translation)
        return translation === key ? '' : translation;
    } catch (error) {
        console.error('Translation error for key:', key, error);
        return '';
    }
}

